def print_three_numbers(first_num, second_num, third_num):
    print(f"first num is : {first_num}")
    print(f"second num is : {second_num}")
    print(f"third num is : {third_num}")


print_three_numbers(1, 4, 2)

print_three_numbers(third_num=2, second_num=4, first_num=1)

print_three_numbers(1, third_num=2, second_num=4)
